# --------------------------------------------
# バージョン比較（将来用）
# --------------------------------------------
def is_newer(current: str, other: str) -> bool:
    """
    other が current より新しいか判定
    """
    def split(v):
        return [int(x) for x in v.split(".")]

    return split(other) > split(current)
